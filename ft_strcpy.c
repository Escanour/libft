#include "libft.h"

char	*ft_strcpy(char *dst, char const *src)
{
	char *a;
	char *b;

	a = dst;
	b = (char *)src;
	while (*b)
	{
		*a = *b;
		a++;
		b++;
	}
	*a = *b;
	return (dst);
}
