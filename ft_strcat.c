#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t		i;
	char		*p2;
	size_t		j;

	p2 = (char *)s2;
	i = ft_strlen(s1);
	j = 0;
	while (p2[j] != '\0')
	{
		s1[i] = p2[j];
		i++;
		j++;
	}
	s1[i] = '\0';
	return (s1);
}
